package gradeone.javase.self05;

/**
 * 工具类
 *      -工具类是为了方便使用，方法定义为静态方法
 *      这样在其他类中可以方便调用
 *      注：静态方法怎么调？
 *          类名.方法名！！！
 */
public class MathUtil {

    public static void sumInt(int a ,int b){
        System.out.println(a + b);
    }
    public static void divide(int a ,int b){
        System.out.println(a / b);
    }
}
